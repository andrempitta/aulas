"""
Programação Orientada a Objetos- POO

- POO é um paradigma de programação que utilisa mapeamento de objetos do mundo real
para modelos computacionais.

- Paradigma de programação é a forma/metodologia utilizada para penser/desenvolver sistemas.

- Principais elementos da Orientação a Objeto
- Classe -> Modelo de objeto do mundo real sendo representado computacionalmente;
- Atributos -> Característica do objeto;
- Método -> Comportamento do objeto (funções);
- Construtor -> Metodo especial para criar objetos;
- Objeto -> Instância da classe.

"""

numero = 10
print(numero)
print(type(numero))

nome = 'Geek'
print(nome)
print(type(nome))


class produto:
    pass

ps4 = produto()
print(ps4)
print(type(ps4))